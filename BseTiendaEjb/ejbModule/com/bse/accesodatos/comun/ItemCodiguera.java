package com.bse.accesodatos.comun;

public class ItemCodiguera {

    private String codigo;
    private String nombre;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /**
     * CONSTRUCTOR
     */
    public ItemCodiguera() {
        this.codigo = null;
        this.nombre = null;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /**
     * CONSTRUCTOR
     * @param codigo
     * @param nombre
     */
    public ItemCodiguera(String codigo, String nombre) {
        this.codigo = codigo;
        this.nombre = nombre;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /**
     *
     */
    @Override
    public String toString() {
      String logueo = null;
      String auxCodigo;
      String auxNombre;

      if (this.getCodigo() == null) { auxCodigo = "null";
      } else { auxCodigo = this.getCodigo(); }

      if (this.getNombre() == null) { auxNombre = "null";
      } else { auxNombre = this.getNombre(); }

        logueo =   "Codigo=[" + auxCodigo
               + "] Nombre=[" + auxNombre
               + "]";

        return logueo;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /**
     * @return the codigo
     */
    public String getCodigo() {
        return codigo;
    }
    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }
    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

}
